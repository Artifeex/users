-- ПРОФИЛЬ СТУДЕНТА
CREATE TABLE IF NOT EXISTS users.student_profiles (
                                                      user_id UUID PRIMARY KEY,
                                                      group_id BIGINT NOT NULL,             -- Обязательно: студент должен быть в группе
                                                      department_id BIGINT,                 -- NULLABLE! Студент может не иметь кафедры (1-2 курс)
                                                      created_at TIMESTAMP NOT NULL,
                                                      created_by VARCHAR(255) NOT NULL,
                                                      updated_at TIMESTAMP NOT NULL,
                                                      updated_by VARCHAR(255) NOT NULL,

                                                      CONSTRAINT fk_student_profile_user FOREIGN KEY (user_id)
                                                          REFERENCES users.users (id) ON DELETE CASCADE,
                                                      CONSTRAINT fk_student_profile_group FOREIGN KEY (group_id)
                                                          REFERENCES users.student_groups (id) ON DELETE RESTRICT,
    -- Важно: Студент выбирает кафедру индивидуально.
    -- Это позволяет студентам одной группы (group_id) иметь разные department_id.
                                                      CONSTRAINT fk_student_profile_dept FOREIGN KEY (department_id)
                                                          REFERENCES users.departments (id) ON DELETE SET NULL
);

-- ПРОФИЛЬ ПРЕПОДАВАТЕЛЯ (Базовый)
CREATE TABLE IF NOT EXISTS users.teacher_profiles (
                                                      user_id UUID PRIMARY KEY,
                                                      department_id BIGINT NOT NULL,
                                                      created_at TIMESTAMP NOT NULL,
                                                      created_by VARCHAR(255) NOT NULL,
                                                      updated_at TIMESTAMP NOT NULL,
                                                      updated_by VARCHAR(255) NOT NULL,

                                                      CONSTRAINT fk_teacher_profile_user FOREIGN KEY (user_id)
                                                          REFERENCES users.users (id) ON DELETE CASCADE,

                                                      CONSTRAINT fk_teacher_profile_department FOREIGN KEY (department_id)
                                                          REFERENCES users.departments(id) ON DELETE RESTRICT -- запрещаем удаление кафедры без явного удаления учителя
);



