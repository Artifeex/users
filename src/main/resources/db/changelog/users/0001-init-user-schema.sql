-- 1. Факультеты / Институты
CREATE TABLE IF NOT EXISTS users.faculties (
                                               id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                                               name VARCHAR(255) NOT NULL,           -- Например: "Институт информационных технологий"
                                               short_name VARCHAR(50) NOT NULL,
                                               created_at TIMESTAMP NOT NULL,
                                               created_by VARCHAR(255) NOT NULL,
                                               updated_at TIMESTAMP NOT NULL,
                                               updated_by VARCHAR(255) NOT NULL
);

-- 2. Кафедры (Привязаны к факультету)
CREATE TABLE IF NOT EXISTS users.departments (
                                                 id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                                                 name VARCHAR(255) NOT NULL,           -- Например: "Кафедра прикладной математики"
                                                 faculty_id BIGINT NOT NULL,
                                                 created_at TIMESTAMP NOT NULL,
                                                 created_by VARCHAR(255) NOT NULL,
                                                 updated_at TIMESTAMP NOT NULL,
                                                 updated_by VARCHAR(255) NOT NULL,
                                                 CONSTRAINT fk_departments_faculty FOREIGN KEY (faculty_id)
                                                     REFERENCES users.faculties (id) ON DELETE RESTRICT
);

-- 3. Направления подготовки (НОВАЯ СУЩНОСТЬ)
-- Студент поступает на направление внутри факультета.
CREATE TABLE IF NOT EXISTS users.fields_of_study (
                                                     id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                                                     code VARCHAR(20) NOT NULL,            -- Например: "09.03.04"
                                                     name VARCHAR(255) NOT NULL,           -- Например: "Программная инженерия"
                                                     faculty_id BIGINT NOT NULL,           -- Направление курирует факультет
                                                     created_at TIMESTAMP NOT NULL,
                                                     created_by VARCHAR(255) NOT NULL,
                                                     updated_at TIMESTAMP NOT NULL,
                                                     updated_by VARCHAR(255) NOT NULL,
                                                     CONSTRAINT fk_fields_faculty FOREIGN KEY (faculty_id)
                                                         REFERENCES users.faculties (id) ON DELETE RESTRICT
);

-- 4. Учебные группы
-- Группа привязана к факультету и конкретному направлению подготовки.
-- Группа НЕ привязана к кафедре жестко, так как студенты внутри могут быть с разных кафедр.
CREATE TABLE IF NOT EXISTS users.student_groups (
                                                    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                                                    name VARCHAR(50) NOT NULL,            -- Например: "ИВТ-401"
                                                    faculty_id BIGINT NOT NULL,           -- Административная привязка (деканат)
                                                    field_of_study_id BIGINT NOT NULL,    -- Какую программу изучают
                                                    created_at TIMESTAMP NOT NULL,
                                                    created_by VARCHAR(255) NOT NULL,
                                                    updated_at TIMESTAMP NOT NULL,
                                                    updated_by VARCHAR(255) NOT NULL,
                                                    CONSTRAINT fk_groups_faculty FOREIGN KEY (faculty_id)
                                                        REFERENCES users.faculties (id) ON DELETE RESTRICT,
                                                    CONSTRAINT fk_groups_field FOREIGN KEY (field_of_study_id)
                                                        REFERENCES users.fields_of_study (id) ON DELETE RESTRICT
);

