CREATE TABLE IF NOT EXISTS users.users (
                                           id UUID PRIMARY KEY,
                                           username VARCHAR(255) NOT NULL UNIQUE, -- логин при аутентификации. Для студентов - это номер студ билета. Для преподавателей может быть табельник сотрудника(в бухгалтерии всем присваивается). Для админа будет admin
                                           email VARCHAR(255) UNIQUE,
                                           password VARCHAR(255) NOT NULL, -- хранит хеш пароля в формате {bcrypt}... для Spring DelegatingPasswordEncoder
                                           first_name VARCHAR(100) NOT NULL,
                                           last_name VARCHAR(100) NOT NULL,
                                           middle_name VARCHAR(100),
                                           is_active BOOLEAN DEFAULT TRUE NOT NULL,
                                           created_at TIMESTAMP NOT NULL,
                                           created_by VARCHAR(255) NOT NULL,
                                           updated_at TIMESTAMP NOT NULL,
                                           updated_by VARCHAR(255) NOT NULL
);

CREATE TABLE IF NOT EXISTS users.roles (
                                           id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                                           name VARCHAR(50) NOT NULL UNIQUE
);

CREATE TABLE IF NOT EXISTS users.users_roles (
                                                 user_id UUID NOT NULL,
                                                 role_id INT NOT NULL,
                                                 PRIMARY KEY (user_id, role_id),
                                                 created_at TIMESTAMP NOT NULL,
                                                 created_by VARCHAR(255) NOT NULL,
                                                 updated_at TIMESTAMP NOT NULL,
                                                 updated_by VARCHAR(255) NOT NULL,
                                                 CONSTRAINT fk_users_roles_user FOREIGN KEY (user_id)
                                                     REFERENCES users.users (id) ON DELETE CASCADE,
                                                 CONSTRAINT fk_users_roles_role FOREIGN KEY (role_id)
                                                     REFERENCES users.roles (id) ON DELETE CASCADE
);

